def lerMensagem():
    arq = open("mensagem.txt","r")

    linhas = arq.readlines()

    contPalavras = str(linhas).split()
    frequencia = []
    
    for p in contPalavras:
        frequencia.append(contPalavras.count(p))
    
    print("Mensagem cifrada:\n" + str(linhas) +"\n")
    print("Contagem:\n" + str(zip(contPalavras, frequencia)))

