def decriptar(k,m):
    aux = ""
    for letra in m:
        aux = aux + k[letra]
    return aux

def montarDicionario(gama1,gama2):
    if len(gama1)!=len(gama2):
        return {}
    else:
        dicionario = {}
        z = zip(gama1,gama2)
        for (x,y) in z:
            dicionario[y] = x
        return dicionario
   
